package model;

public class UserInfo {
    
    public String Login;
    public String Pwd;
    
    
    public UserInfo() {
        super();
    }

    public void setLogin(String Login) {
        this.Login = Login;
    }

    public String getLogin() {
        return Login;
    }

    public void setPwd(String Pwd) {
        this.Pwd = Pwd;
    }

    public String getPwd() {
        return Pwd;
    }
}
